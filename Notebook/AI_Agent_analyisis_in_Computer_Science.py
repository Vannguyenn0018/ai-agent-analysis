# Bước 1.1: Chuyển đổi từ dạng khoảng (Text) sang dạng số liên tục (Midpoint)
def convert_income_to_numeric(val):
    if pd.isna(val):
        return np.nan
    val = str(val).strip()

    # Ánh xạ theo các khoảng xuất hiện trong file metadata của bạn
    if '30-60K' in val: return 45000
    elif '60-86K' in val: return 73000
    elif '86K-165K' in val: return 125500
    elif 'under 30K' in val or '<30K' in val: return 15000
    elif 'above 165K' in val or '>165K' in val: return 210000 # Ước lượng biên trên
    return np.nan

final_merged_df['Income_Numeric'] = final_merged_df['Income'].apply(convert_income_to_numeric)

# Bước 1.2: Áp dụng phương pháp xử lý ngoại lai nghiêm ngặt cho Income
# Cách A: Winsorize - Cắt ngọn và đưa về phân vị 1% và 99% (Tối ưu nhất cho hồi quy)
final_merged_df['Income_Cleaned'] = mstats.winsorize(final_merged_df['Income_Numeric'], limits=[0.01, 0.01])

# =========================================================================
# CHÂN TRANG 2: XỬ LÝ BIẾN LLM FAMILIARITY (Mức độ quen thuộc)
