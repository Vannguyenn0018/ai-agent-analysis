"""### **XỬ LÝ NGOẠI LAI (OUTLIERS)**

**1. Biến Thu nhập (Income)**

Do biến Thu nhập (Income) ban đầu được thu thập dưới dạng biến khoảng, nghiên cứu đã tiến hành kỹ thuật chuyển đổi sang biến liên tục bằng giá trị trung vị của khoảng (Midpoint transformation). Nhằm triệt tiêu ảnh hưởng của các giá trị ngoại lai cực đoan có thể làm chệch hệ số góc của đường hồi quy, nghiên cứu áp dụng phương pháp Winsorization tại phân vị 1% và 99%. Kết quả giúp đưa độ lệch (Skewness) của biến thu nhập về mức an toàn, đảm bảo giả định phân phối chuẩn trong mô hình OLS.

**2. Biến LLM Familiarity:**

Biến Mức độ quen thuộc với AI (LLM Familiarity) được chuẩn hóa sang thang đo thứ bậc từ 1 đến 5. Để ngăn chặn hiện tượng phân tách hoàn toàn (Complete Separation) hoặc tăng sai số chuẩn do các nhóm thiểu số gây ra, các biểu mẫu quan sát thuộc các nhóm có tần suất xuất hiện cực thấp (< 5%) đã được gộp một cách hợp lý vào nhóm liền kề nhằm đảm bảo tính vững của các ước lượng hồi quy.
"""

