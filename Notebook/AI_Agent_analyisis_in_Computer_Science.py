# Bước 2.1: Chuyển đổi biến phân loại thứ bậc sang Thang đo số (1 -> 4)
# Kiểm tra chuỗi ký tự thực tế trong file metadata của bạn để ánh xạ chính xác
familiarity_map = {
    'I have never heard of them': 1,
    'I know what they are, but have never used them': 2,
    'I have some experience using them.': 3,
    'I use them regularly.': 4,
    'I use them every day in my work.': 5
}
final_merged_df['LLM_Familiarity_Numeric'] = final_merged_df['LLM Familiarity'].map(familiarity_map)

# Bước 2.2: Xử lý "ngoại lai" dạng tần suất thấp (Rare Categories) cho biến thứ bậc
# Tính toán tỷ lệ phần trăm của từng nhóm phản hồi
frequencies = final_merged_df['LLM_Familiarity_Numeric'].value_counts(normalize=True)
print("\nPhân phối tỷ lệ của biến LLM Familiarity:\n", frequencies)

# NẾU CÓ NHÓM CÓ TỶ LỆ < 5% (Ví dụ nhóm 1 hoặc nhóm 5 quá ít người chọn):
# Tiến hành gộp nhóm đó vào nhóm liền kề để tránh làm nhiễu sai số chuẩn (Standard Error) trong hồi quy
# Ví dụ dưới đây: Nếu nhóm 1 (chưa từng nghe qua) quá ít, gộp chung với nhóm 2 thành nhóm "Ít/Chưa quen thuộc"
final_merged_df['LLM_Familiarity_Cleaned'] = final_merged_df['LLM_Familiarity_Numeric'].replace({1: 2})


# =========================================================================
# BƯỚC KHỬ TRÙNG CUỐI CÙNG: KIỂM TRA ĐỘ LỆCH (SKEWNESS)
# =========================================================================
print("\n--- KIỂM TRA ĐỘ SẠCH CỦA HỆ SỐ ---")
