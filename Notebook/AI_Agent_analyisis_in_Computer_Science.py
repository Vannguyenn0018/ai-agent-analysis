"""### **3.2 Hành vi sử dụng AI theo Chuyên môn (Behavioral Breakdown)**

"""

# 1. Filter data for Computer Science occupations
cs_keywords = ['Software', 'Computer', 'Data', 'Programmer', 'Developer']
cs_llm_usage_df = final_merged_df[final_merged_df['Occupation'].str.contains('|'.join(cs_keywords), na=False)].copy()

# 2. Define LLM Usage Type columns and ordinal mapping
llm_usage_cols_for_radar = [
    'LLM Usage by Type - Information Access',
    'LLM Usage by Type - Edit',
    'LLM Usage by Type - Idea Generation',
    'LLM Usage by Type - Communication',
    'LLM Usage by Type - Analysis',
    'LLM Usage by Type - Decision',
    'LLM Usage by Type - Coding',
    'LLM Usage by Type - System Design',
    'LLM Usage by Type - Data Processing'
]

# Ordinal mapping based on user's request
llm_usage_ordinal_map = {
    'Never': 1,
    'Rarely': 2, # Assuming 'Rarely' might exist or for completeness based on common scales
    'Monthly': 3,
    'Weekly': 4,
    'Daily': 5
}

# 3. Apply ordinal encoding and handle NaNs
for col in llm_usage_cols_for_radar:
    # Fillna with 0 for cases where there's no response, implying no usage or not applicable,
    # then map existing text values to ordinal scores.
    # For any values not in the map (e.g., actual NaNs not handled by previous fills, or unexpected text), they will become NaN here.
    # Then fill those final NaNs with 0, effectively meaning no usage if they weren't explicitly 'Never'.
    cs_llm_usage_df[col + '_Score'] = cs_llm_usage_df[col].map(llm_usage_ordinal_map).fillna(0)

# Extract the score columns for aggregation
score_cols = [col + '_Score' for col in llm_usage_cols_for_radar]

# 4. Calculate mean scores for each usage type
mean_usage_scores = cs_llm_usage_df[score_cols].mean()

# Rename columns for better plotting labels
plot_labels = [
    'Information Access',
    'Edit',
    'Idea Generation',
    'Communication',
    'Analysis',
    'Decision',
    'Coding',
    'System Design',
    'Data Processing'
]
mean_usage_scores.index = plot_labels

# 5. Prepare data for Radar Chart
values = mean_usage_scores.values
N = len(plot_labels)

# What will be the angle of each axis in the plot? (divide the plot / number of variable)
angles = [n / float(N) * 2 * np.pi for n in range(N)]
angles += angles[:1] # Repeat the first value to close the circle

# Repeat the first value to close the circle
values = np.append(values, values[:1])

# 6. Create the Radar Chart
fig, ax = plt.subplots(figsize=(10, 10), subplot_kw=dict(polar=True))

# Plot data
ax.plot(angles, values, color='mediumseagreen', linewidth=2, linestyle='solid') # Changed color to 'mediumseagreen'
ax.fill(angles, values, color='lightgreen', alpha=0.4) # Changed fill color to 'lightgreen'

# Add labels to axes
ax.set_theta_offset(np.pi / 2)
ax.set_theta_direction(-1)
plt.xticks(angles[:-1], plot_labels, color='grey', size=12)

# Draw ylabels
ax.set_rlabel_position(0)
plt.yticks([1, 2, 3, 4, 5], ["Never (1)", "Rarely (2)", "Monthly (3)", "Weekly (4)", "Daily (5)"], color="grey", size=10)
plt.ylim(0, 5)

plt.title('Average LLM Usage Behavior by Type in Computer Science Occupations', size=16, color='black', y=1.1)
plt.show()

