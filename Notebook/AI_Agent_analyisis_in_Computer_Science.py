"""# **2. Feature Engineering**

## **2.1 Biến Generation (Thế hệ): Chuyển đổi cột Age thành biến phân loại.**
- Dưới 28 tuổi: Gen Z (Đại diện cho nhóm lao động trẻ, xu hướng tìm kiếm cân bằng).

- 28 - 43 tuổi: Millennials (Nhóm chuyển giao).

- Trên 43 tuổi: Gen X+ (Nhóm mang đậm văn hóa lao động truyền thống "cần cù bù thông minh").

Biến này đại diện cho sự khác biệt về bối cảnh xã hội và văn hóa lao động giữa các thế hệ.
"""

def classify_generation(age):
    if age < 28:
        return 'Gen Z'
    elif 28 <= age <= 43:
        return 'Millennials'
    else:
        return 'Gen X+'

